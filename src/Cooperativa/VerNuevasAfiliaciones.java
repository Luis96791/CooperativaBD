    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
        // TODO add your handling code here:
        
        if(this.tabla_nuevas_afiliaciones.getRowCount() == 0 && this.tabla_nuevas_afiliaciones.getSelectedRow() == -1)
        {
            try
            {
                conexion.callableStatement = conexion.connection.prepareCall("SELECT * FROM FN_NUEVAS_AFILIACIONES(?)");
                conexion.callableStatement.setInt(1, Integer.parseInt(text_anio.getText()));
                conexion.resultSet = conexion.callableStatement.executeQuery();
                
                while(conexion.resultSet.next())
                {
                    modelo_tabla.addRow(new Object[]
                    {
                        ++autoincrementable,
                        conexion.resultSet.getInt("CODIGO_EMPLEADO"),
                        conexion.resultSet.getString("NOMBRES"),
                        conexion.resultSet.getString("FECHA_CONTRATACION"),
                        conexion.resultSet.getDouble("APORTACIONES"),
                        conexion.resultSet.getDouble("AHORRO"),
                        conexion.resultSet.getDouble("TOTAL")
                    });
                }
                conexion.callableStatement.close();
                conexion.resultSet.close();
            }
            catch(SQLException | NumberFormatException e){}
        }
    }//GEN-LAST:event_btnVerActionPerformed

