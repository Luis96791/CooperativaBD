    public boolean buscarEmpleado(int codigo_empleado)
    {
        try
        {
            conexion.callableStatement = conexion.connection.prepareCall("{call SP_PRESTAMO_BUSCAR_CODIGO_EMPLEADO(?,?)}");
            conexion.callableStatement.setInt(1, codigo_empleado);
            conexion.callableStatement.registerOutParameter(2, java.sql.Types.BIGINT);
            conexion.callableStatement.execute();
            
            if(conexion.callableStatement.getInt(2) == codigo_empleado)
            {
                leerDatosPrestamo(codigo_empleado);
                return true;
            }
        }catch(Exception e){}
        return false;
    }
    
    public void leerDatosPrestamo(int codigo_empleado)
    {
        String datos[] = new String[8];
        try
        {
            conexion.callableStatement = conexion.connection.prepareCall("{call SP_PRESTAMO_READ(?)}");
            conexion.callableStatement.setInt(1, codigo_empleado);
            conexion.resultSet = conexion.callableStatement.executeQuery();
            
            while(conexion.resultSet.next())
            {
                datos[0] = conexion.resultSet.getString("NUMERO_PRESTAMO");
                datos[1] = conexion.resultSet.getString("FECHA_PRESTAMO");
                datos[2] = conexion.resultSet.getString("TIPO_PRESTAMO");
                datos[3] = String.valueOf(conexion.resultSet.getDouble("MONTO_PRESTAMO"));
                datos[4] = String.valueOf(conexion.resultSet.getInt("PERIODOS_PRESTAMO"));
                datos[5] = String.valueOf(conexion.resultSet.getInt("NUMERO_PERIODO"));
                datos[6] = String.valueOf(conexion.resultSet.getDouble("CUOTA_MENSUAL"));
                datos[7] = String.valueOf(conexion.resultSet.getDouble("SALDO_PRESTAMO"));
                setDatosInTextBoxs(datos);
            }
            conexion.callableStatement.close();
            conexion.resultSet.close();
        }catch(Exception e){}
    }
    
    public void setDatosInTextBoxs(String[] datos) 
    {
        text_numero_prestamo.setText(datos[0]);
        text_fecha_prestamo.setText(datos[1]);
        text_tipo_prestamo.setText(datos[2]);
        text_monto_prestado.setText(datos[3]);
        text_periodos_prestamo.setText(datos[4]);
        text_periodo_actual.setText(datos[5]);
        text_cuota_mensual.setText(datos[6]);
        text_saldo_actual.setText(datos[7]);
    }

