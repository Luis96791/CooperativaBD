    public boolean buscarEmpleado(int codigo_empleado, String tipo_cuenta)
    {
        try
        {
            conexion.callableStatement = conexion.connection.prepareCall("{call SP_EMPLEADO_BUSCAR(?,?)}");
            conexion.callableStatement.setInt(1, codigo_empleado);
            conexion.callableStatement.registerOutParameter(2, java.sql.Types.BIGINT);
            conexion.callableStatement.execute();
            
            if(conexion.callableStatement.getInt(2) != 0)
            {
                leerDatosCuenta(codigo_empleado, tipo_cuenta);
                return true;
            }
        }catch(Exception e){}
        return false;
    }
    
    public void leerDatosCuenta(int codigo_empleado, String tipo_cuenta)
    {
        String datos[] = new String[5];
        try
        {
            conexion.callableStatement = conexion.connection.prepareCall("{call SP_CUENTA_READ(?,?)}");
            conexion.callableStatement.setInt(1, codigo_empleado);
            conexion.callableStatement.setString(2, tipo_cuenta);
            conexion.resultSet = conexion.callableStatement.executeQuery();
            
            while(conexion.resultSet.next())
            {
                datos[0] = conexion.resultSet.getString("NUMERO_CUENTA");
                datos[1] = conexion.resultSet.getString("FECHA_APERTURA");
                datos[2] = String.valueOf(conexion.resultSet.getDouble("AHORRO_MENSUAL"));
                datos[3] = String.valueOf(conexion.resultSet.getInt("ANTIGUEDAD"));
                datos[4] = String.valueOf(conexion.resultSet.getDouble("SALDO_CUENTA"));
                setDatosInTextBoxs(datos);
            }
            conexion.callableStatement.close();
            conexion.resultSet.close();
        }catch(Exception e){}
    }
    
    public void setDatosInTextBoxs(String[] datos) 
    {
        text_numero_cuenta.setText(datos[0]);
        text_fecha_apertura.setText(datos[1]);
        text_ahorro_mensual.setText(datos[2]);
        text_antiguedad.setText(datos[3]);
        text_saldo_cuenta.setText(datos[4]);
    }

